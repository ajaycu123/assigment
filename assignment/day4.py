# integer
print("int(205) is:", int(205))

# string
print("int('205') is:", int('205'))


# float
print("int(205.25) is:", int(205.25))

# tuple
mytuple = ('A', 'B', 'C', 'D', 'E', 'F', 'G')
# First value
print(mytuple[0])
# Second value
print(mytuple[1])
mytuple = ('A', 'B', 'C', 'D', 'E', 'F', 'G')
print(mytuple[1:3])


# Boolean
x = "Hello"
y = 15
print(bool(x))
print(bool(y))


# dictionary
Dict = {'Tim': 18,'Charlie':12,'Tiffany':22,'Robert':25}
print (Dict['Tiffany'])





